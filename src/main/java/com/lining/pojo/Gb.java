package com.lining.pojo;

public class Gb {
	private int gbid;
	private int gid;
	private int bid;
	public Gb() {
		super();
	}
	public Gb(int gbid, int gid, int bid) {
		super();
		this.gbid = gbid;
		this.gid = gid;
		this.bid = bid;
	}
	public int getGbid() {
		return gbid;
	}
	public void setGbid(int gbid) {
		this.gbid = gbid;
	}
	public int getGid() {
		return gid;
	}
	public void setGid(int gid) {
		this.gid = gid;
	}
	public int getBid() {
		return bid;
	}
	public void setBid(int bid) {
		this.bid = bid;
	}
	
}
