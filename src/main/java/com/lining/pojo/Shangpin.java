package com.lining.pojo;

public class Shangpin {
	private int gid;
	private String gname;
	private double gprice;
	private double gdiscount;
	private String gdes;
	private String gtype;
	private String gimg;
	private String gydate;
	private String gtime;
	private String fdate;
	private String developer;
	private String publisher;
	private String ximg;
	private String xdes;
	public Shangpin() {
		super();
	}
	public Shangpin(int gid, String gname, double gprice, double gdiscount, String gdes, String gtype, String gimg,
			String gydate, String gtime, String fdate, String developer, String publisher, String ximg, String xdes) {
		super();
		this.gid = gid;
		this.gname = gname;
		this.gprice = gprice;
		this.gdiscount = gdiscount;
		this.gdes = gdes;
		this.gtype = gtype;
		this.gimg = gimg;
		this.gydate = gydate;
		this.gtime = gtime;
		this.fdate = fdate;
		this.developer = developer;
		this.publisher = publisher;
		this.ximg = ximg;
		this.xdes = xdes;
	}
	public int getGid() {
		return gid;
	}
	public void setGid(int gid) {
		this.gid = gid;
	}
	public String getGname() {
		return gname;
	}
	public void setGname(String gname) {
		this.gname = gname;
	}
	public double getGprice() {
		return gprice;
	}
	public void setGprice(double gprice) {
		this.gprice = gprice;
	}
	public double getGdiscount() {
		return gdiscount;
	}
	public void setGdiscount(double gdiscount) {
		this.gdiscount = gdiscount;
	}
	public String getGdes() {
		return gdes;
	}
	public void setGdes(String gdes) {
		this.gdes = gdes;
	}
	public String getGtype() {
		return gtype;
	}
	public void setGtype(String gtype) {
		this.gtype = gtype;
	}
	public String getGimg() {
		return gimg;
	}
	public void setGimg(String gimg) {
		this.gimg = gimg;
	}
	public String getGydate() {
		return gydate;
	}
	public void setGydate(String gydate) {
		this.gydate = gydate;
	}
	public String getGtime() {
		return gtime;
	}
	public void setGtime(String gtime) {
		this.gtime = gtime;
	}
	public String getFdate() {
		return fdate;
	}
	public void setFdate(String fdate) {
		this.fdate = fdate;
	}
	public String getDeveloper() {
		return developer;
	}
	public void setDeveloper(String developer) {
		this.developer = developer;
	}
	public String getPublisher() {
		return publisher;
	}
	public void setPublisher(String publisher) {
		this.publisher = publisher;
	}
	public String getXimg() {
		return ximg;
	}
	public void setXimg(String ximg) {
		this.ximg = ximg;
	}
	public String getXdes() {
		return xdes;
	}
	public void setXdes(String xdes) {
		this.xdes = xdes;
	}
	
	
}
